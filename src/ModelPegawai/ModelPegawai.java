public class ModelPegawai {
     private String NIP, NamaPegawai, Password, AlamatPegawai, NomorTeleponPegawai;

    public String getNIP() {
        return NIP;
    }

    public void setNIP(String NIP) {
        this.NIP = NIP;
    }

    public String getNamaPegawai() {
        return NamaPegawai;
    }

    public void setNamaPegawai(String NamaPegawai) {
        this.NamaPegawai = NamaPegawai;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String Password) {
        this.Password = Password;
    }

    public String getAlamatPegawai() {
        return AlamatPegawai;
    }

    public void setAlamatPegawai(String AlamatPegawai) {
        this.AlamatPegawai = AlamatPegawai;
    }

    public String getNomorTeleponPegawai() {
        return NomorTeleponPegawai;
    }

    public void setNomorTeleponPegawai(String NomorTeleponPegawai) {
        this.NomorTeleponPegawai = NomorTeleponPegawai;
    }

    
}
